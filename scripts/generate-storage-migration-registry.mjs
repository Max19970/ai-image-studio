#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const check = process.argv.includes('--check');
const migrationsDir = path.join(root, 'server/storage/migrations');
const outputPath = path.join(migrationsDir, 'registry.generated.ts');

function toPosix(value) {
  return value.replace(/\\/g, '/');
}

function discoverStorageMigrations() {
  return fs
    .readdirSync(migrationsDir, { withFileTypes: true })
    .filter((entry) => entry.isFile() && /^\d{3}_.+\.ts$/.test(entry.name))
    .map((entry) => {
      const migrationPath = path.join(migrationsDir, entry.name);
      const source = fs.readFileSync(migrationPath, 'utf8');
      const match = source.match(/export\s+const\s+(\w+Migration)\b/) ?? source.match(/export\s+const\s+(\w+)\s*[:=]/);
      if (!match) throw new Error(`Could not find exported migration descriptor in ${toPosix(path.relative(root, migrationPath))}`);
      const basename = entry.name.replace(/\.ts$/, '');
      return {
        id: basename,
        exportName: match[1],
        importPath: `./${basename}`,
        sourcePath: `./${entry.name}`
      };
    })
    .sort((a, b) => a.id.localeCompare(b.id));
}

function render(migrations) {
  const imports = migrations.map((migration) => `import { ${migration.exportName} } from '${migration.importPath}';`).join('\n');
  const entries = migrations.map((migration) => `  '${migration.sourcePath}': { ${migration.exportName} }`).join(',\n');
  return `// This file is generated by scripts/generate-storage-migration-registry.mjs.\n// Keep storage migrations in server/storage/migrations/NNN_*.ts; regenerate when adding/removing migrations.\n${imports}\nimport type { StorageMigrationDescriptor } from './types';\n\nexport const storageMigrationGeneratedModules: Record<string, Record<string, StorageMigrationDescriptor>> = {\n${entries}\n};\n`;
}

const next = render(discoverStorageMigrations());
const current = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, 'utf8') : '';

if (check) {
  if (current.replace(/\r\n/g, '\n') !== next.replace(/\r\n/g, '\n')) {
    console.error('Storage migration generated registry is stale. Run npm run storage:migrations:generate.');
    process.exit(1);
  }
  console.log('Storage migration generated registry is up to date.');
} else {
  fs.writeFileSync(outputPath, next);
  console.log(`Wrote ${toPosix(path.relative(root, outputPath))}`);
}
