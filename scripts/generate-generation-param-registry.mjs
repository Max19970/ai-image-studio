#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const check = process.argv.includes('--check');
const outputPath = path.join(root, 'src/entities/generation-params/logicalRegistry.generated.ts');
const fieldsDir = path.join(root, 'src/entities/generation-params/fields');

function toPosix(value) {
  return value.replace(/\\/g, '/');
}

function walk(dir, files = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const absolute = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      walk(absolute, files);
      continue;
    }
    if (entry.isFile() && entry.name === 'param.ts') files.push(absolute);
  }
  return files;
}

function discoverParams() {
  return walk(fieldsDir)
    .map((absolutePath) => {
      const source = fs.readFileSync(absolutePath, 'utf8');
      const match = source.match(/export\s+const\s+(\w+)\s*[:=]/);
      if (!match) throw new Error(`Could not find exported param definition in ${toPosix(path.relative(root, absolutePath))}`);
      const relativeFromParams = toPosix(path.relative(path.join(root, 'src/entities/generation-params'), absolutePath));
      const importPath = `./${relativeFromParams.replace(/\.ts$/, '')}`;
      return {
        exportName: match[1],
        importPath,
        sourcePath: `./${relativeFromParams}`
      };
    })
    .sort((a, b) => a.sourcePath.localeCompare(b.sourcePath));
}

function render(params) {
  const imports = params
    .map((param) => `import { ${param.exportName} } from '${param.importPath}';`)
    .join('\n');
  const entries = params
    .map((param) => `  '${param.sourcePath}': { ${param.exportName} }`)
    .join(',\n');
  return `// This file is generated by scripts/generate-generation-param-registry.mjs.\n// Keep logical parameter definitions in src/entities/generation-params/fields/**/param.ts.\n${imports}\nimport type { GenerationParamDefinition } from './types';\n\nexport const logicalGenerationParamGeneratedModules: Record<string, Record<string, GenerationParamDefinition>> = {\n${entries}\n};\n`;
}

const next = render(discoverParams());
if (check) {
  const current = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, 'utf8') : '';
  if (current.replace(/\r\n/g, '\n') !== next.replace(/\r\n/g, '\n')) {
    console.error('Generation parameter generated registry is stale. Run npm run registry:generate.');
    process.exit(1);
  }
  console.log('Generation parameter generated registry is up to date.');
} else {
  fs.writeFileSync(outputPath, next);
  console.log(`Wrote ${toPosix(path.relative(root, outputPath))}`);
}
