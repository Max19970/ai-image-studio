#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const check = process.argv.includes('--check');

const registries = [
  {
    label: 'client provider',
    outputPath: path.join(root, 'src/entities/provider/registry.generated.ts'),
    providersDir: path.join(root, 'src/providers'),
    manifestType: 'ProviderClientManifest',
    generatedConst: 'providerClientManifestGeneratedModules',
    importPathFor: (entryName) => `../../providers/${entryName}/manifest`,
    sourcePathFor: (entryName) => `../../providers/${entryName}/manifest.ts`,
    header: 'Keep provider manifests in src/providers/*/manifest.ts; regenerate when adding/removing adapters.'
  },
  {
    label: 'server provider',
    outputPath: path.join(root, 'server/providers/registry.generated.ts'),
    providersDir: path.join(root, 'server/providers'),
    manifestType: 'ProviderServerManifest',
    generatedConst: 'providerServerManifestGeneratedModules',
    importPathFor: (entryName) => `./${entryName}/manifest`,
    sourcePathFor: (entryName) => `./${entryName}/manifest.ts`,
    header: 'Keep provider manifests in server/providers/*/manifest.ts; regenerate when adding/removing adapters.'
  }
];

function toPosix(value) {
  return value.replace(/\\/g, '/');
}

function discoverProviderManifests(registry) {
  return fs
    .readdirSync(registry.providersDir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => {
      const manifestPath = path.join(registry.providersDir, entry.name, 'manifest.ts');
      if (!fs.existsSync(manifestPath)) return null;
      const source = fs.readFileSync(manifestPath, 'utf8');
      const match = source.match(/export\s+const\s+(\w+Provider(?:Client|Server)Manifest)\b/)
        ?? source.match(/export\s+const\s+(\w+)\s*[:=]/);
      if (!match) throw new Error(`Could not find exported manifest in ${toPosix(path.relative(root, manifestPath))}`);
      return {
        id: entry.name,
        exportName: match[1],
        importPath: registry.importPathFor(entry.name),
        sourcePath: registry.sourcePathFor(entry.name)
      };
    })
    .filter(Boolean)
    .sort((a, b) => a.id.localeCompare(b.id));
}

function render(registry, manifests) {
  const imports = manifests
    .map((manifest) => `import { ${manifest.exportName} } from '${manifest.importPath}';`)
    .join('\n');
  const entries = manifests
    .map((manifest) => `  '${manifest.sourcePath}': { ${manifest.exportName} }`)
    .join(',\n');
  return `// This file is generated by scripts/generate-provider-registry.mjs.\n// ${registry.header}\n${imports}\nimport type { ${registry.manifestType} } from './manifest';\n\nexport const ${registry.generatedConst}: Record<string, Record<string, ${registry.manifestType}>> = {\n${entries}\n};\n`;
}

const outputs = registries.map((registry) => ({ registry, next: render(registry, discoverProviderManifests(registry)) }));

if (check) {
  let stale = false;
  for (const { registry, next } of outputs) {
    const current = fs.existsSync(registry.outputPath) ? fs.readFileSync(registry.outputPath, 'utf8') : '';
    if (current.replace(/\r\n/g, '\n') !== next.replace(/\r\n/g, '\n')) {
      console.error(`${registry.label} generated registry is stale. Run npm run registry:generate.`);
      stale = true;
    }
  }
  if (stale) process.exit(1);
  console.log('Provider generated registries are up to date.');
} else {
  for (const { registry, next } of outputs) {
    fs.writeFileSync(registry.outputPath, next);
    console.log(`Wrote ${toPosix(path.relative(root, registry.outputPath))}`);
  }
}
