#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const check = process.argv.includes('--check');
const outputPath = path.join(root, 'src/entities/provider/registry.generated.ts');
const providersDir = path.join(root, 'src/providers');

function toPosix(value) {
  return value.replace(/\\/g, '/');
}

function pascalCase(value) {
  return value
    .split(/[^a-z0-9]+/i)
    .filter(Boolean)
    .map((part) => `${part[0]?.toUpperCase() ?? ''}${part.slice(1)}`)
    .join('');
}

function discoverProviderManifests() {
  return fs
    .readdirSync(providersDir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => {
      const manifestPath = path.join(providersDir, entry.name, 'manifest.ts');
      if (!fs.existsSync(manifestPath)) return null;
      const source = fs.readFileSync(manifestPath, 'utf8');
      const match = source.match(/export\s+const\s+(\w+ProviderClientManifest)\b/)
        ?? source.match(/export\s+const\s+(\w+)\s*[:=]/);
      if (!match) throw new Error(`Could not find exported client manifest in ${toPosix(path.relative(root, manifestPath))}`);
      return {
        id: entry.name,
        exportName: match[1],
        importPath: `../../providers/${entry.name}/manifest`,
        sourcePath: `../../providers/${entry.name}/manifest.ts`
      };
    })
    .filter(Boolean)
    .sort((a, b) => a.id.localeCompare(b.id));
}

function render(manifests) {
  const imports = manifests
    .map((manifest) => `import { ${manifest.exportName} } from '${manifest.importPath}';`)
    .join('\n');
  const entries = manifests
    .map((manifest) => `  '${manifest.sourcePath}': { ${manifest.exportName} }`)
    .join(',\n');
  const generatedAt = pascalCase('provider registry');
  void generatedAt;
  return `// This file is generated by scripts/generate-provider-registry.mjs.\n// Keep provider manifests in src/providers/*/manifest.ts; regenerate when adding/removing adapters.\n${imports}\nimport type { ProviderClientManifest } from './manifest';\n\nexport const providerClientManifestGeneratedModules: Record<string, Record<string, ProviderClientManifest>> = {\n${entries}\n};\n`;
}

const next = render(discoverProviderManifests());
if (check) {
  const current = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, 'utf8') : '';
  if (current.replace(/\r\n/g, '\n') !== next.replace(/\r\n/g, '\n')) {
    console.error('Provider generated registry is stale. Run npm run registry:generate.');
    process.exit(1);
  }
  console.log('Provider generated registry is up to date.');
} else {
  fs.writeFileSync(outputPath, next);
  console.log(`Wrote ${toPosix(path.relative(root, outputPath))}`);
}
