#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const check = process.argv.includes('--check');

function toPosix(value) {
  return value.replace(/\\/g, '/');
}

function toRelativeImport(fromFile, toFile) {
  let relative = toPosix(path.relative(path.dirname(fromFile), toFile)).replace(/\.(tsx?|json)$/, '');
  if (!relative.startsWith('.')) relative = `./${relative}`;
  return relative;
}

function walk(dir, predicate = () => true) {
  if (!fs.existsSync(dir)) return [];
  const out = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) out.push(...walk(full, predicate));
    else if (predicate(full)) out.push(full);
  }
  return out;
}

function camelCase(value) {
  const parts = value.split(/[^a-z0-9]+/i).filter(Boolean);
  return parts.map((part, index) => {
    const lower = `${part[0]?.toLowerCase() ?? ''}${part.slice(1)}`;
    return index === 0 ? lower : `${lower[0]?.toUpperCase() ?? ''}${lower.slice(1)}`;
  }).join('');
}

function identifierFromFile(file, suffix = 'Module') {
  const relative = toPosix(path.relative(root, file)).replace(/\.(tsx?|json)$/, '');
  return `${camelCase(relative)}${suffix}`.replace(/[^a-zA-Z0-9_$]/g, '');
}

function generatedHeader(scriptName) {
  return `// This file is generated by scripts/${scriptName}.\n// Add/remove descriptor modules; do not edit this fallback registry by hand.\n`;
}

function renderModuleMap({ outputPath, files, exportName, typeImport, typeName, sourcePath }) {
  const imports = files.map((file) => `import * as ${identifierFromFile(file)} from '${toRelativeImport(outputPath, file)}';`).join('\n');
  const importedTypeName = typeName?.split('<')[0] ?? null;
  const typeLine = typeImport && importedTypeName ? `import type { ${importedTypeName} } from '${typeImport}';\n` : '';
  const recordType = 'Record<string, Record<string, unknown>>';
  const entries = files.map((file) => `  '${sourcePath(file)}': ${identifierFromFile(file)}`).join(',\n');
  return `${generatedHeader('generate-extension-registries.mjs')}${imports}\n${typeLine}\nexport const ${exportName}: ${recordType} = {\n${entries}\n};\n`;
}

function renderFeatureFallback(outputPath) {
  const files = walk(path.join(root, 'src/shared/features/descriptors'), (file) => file.endsWith('.feature.ts')).sort();
  return renderModuleMap({
    outputPath,
    files,
    exportName: 'featureDescriptorFallbackModules',
    typeImport: './types',
    typeName: 'AppFeatureDescriptor',
    sourcePath: (file) => `./descriptors/${path.basename(file)}`
  });
}

