function renderI18nFallback(outputPath) {
  const localesDir = path.join(root, 'src/shared/i18n/locales');
  const localeDirs = fs.readdirSync(localesDir, { withFileTypes: true }).filter((entry) => entry.isDirectory()).map((entry) => entry.name).sort();
  const imports = [];
  const entries = [];
  for (const locale of localeDirs) {
    const dir = path.join(localesDir, locale);
    imports.push(`import { locale as ${locale}Locale } from './locales/${locale}/locale';`);
    const jsonFiles = fs.readdirSync(dir).filter((name) => name.endsWith('.json')).sort();
    const vars = [];
    for (const file of jsonFiles) {
      const varName = `${locale}${camelCase(file.replace(/\.json$/, ''))}`;
      imports.push(`import ${varName} from './locales/${locale}/${file}';`);
      vars.push(varName);
    }
    entries.push(`  './locales/${locale}/index.ts': {\n    locale: ${locale}Locale,\n    dictionary: mergeDictionary(${vars.join(', ')})\n  }`);
  }
  return `${generatedHeader('generate-extension-registries.mjs')}${imports.join('\n')}\nimport type { Dictionary, LocaleDictionaryModule } from './types';\n\nfunction mergeDictionary(...parts: Dictionary[]): Dictionary {\n  return Object.assign({}, ...parts);\n}\n\nexport const localeFallbackModules: Record<string, LocaleDictionaryModule> = {\n${entries.join(',\n')}\n};\n`;
}

